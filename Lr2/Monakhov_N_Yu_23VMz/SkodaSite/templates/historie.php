            <h3>История возникновения Skoda</h3>
        </div>
        <div class="model_text">
            <a>Škoda Auto a.s. — чешская автомобилестроительная компания, выпускающая легковые автомобили под торговой маркой Škoda. Штаб-квартира компании находится в городе Млада-Болеслав (Чехия). Логотип компании «крылатая стрела» был впервые представлен в 1926 году.

История производства автомобилей, носящих в настоящее время марку Шкода, началась с конца 1895 года в чешском городе Млада-Болеслав. Механик Вацлав Лаурин и книготорговец Вацлав Клемент, оба увлечённые велосипедисты, начали выпускать велосипеды собственной конструкции, назвав их патриотично — Славия.

Их дела шли благополучно. К 1899 году фирма Laurin & Klement Co. начала производство мотоциклов. Гонщики одержали первые победы в международных соревнованиях.

На рубеже веков компания предприняла ещё один важный шаг — был взят курс на производство автомобилей.

Уже самый первый автомобиль, Voiturette A, выпущенный в 1905 году, имел коммерческий успех, тем самым став одним из символов истории чешского автомобилестроения. Благодаря своим ноу-хау и опыту, компания очень скоро заняла стабильное положение на развивающихся мировых рынках. Laurin & Klement способствовало утверждению позиций Богемского королевства как наиболее промышленно развитого региона Австро-Венгерской монархии.

В ходе развития фирме стало тесно в рамках семейного предприятия. Пришло время перейти на новый уровень. В 1907 году основатели преобразовали L & K в акционерное общество. Прогресс привёл к созданию новой модели — Faeton. После 1914 года компания принимала участие в производстве военной продукции.

Для укрепления и сохранения своих позиций, а также модернизации производства, L & K искали сильного партнёра. Производство автомобилей под маркой Skoda в промежуток между 1925 и 1929 годами составило 2985 штук. В годы Второй мировой войны выпуск автомобилей заметно сократился.

15 декабря 1923 года в чешском городе Пльзень зарегистрирована эмблема автомобилестроительной фирмы «Шкода». В 1869 году молодой инженер Эмиль Шкода приобрел небольшой механический завод в городе Пльзень. Со временем завод превратился в одно из крупнейших машиностроительных предприятий Чехословакии.

27 июня 1925 года L & K вошла в состав крупнейшего промышленного предприятия региона, «Шкода-Пльзень» (Škoda Plzeň). На этом закончилась история марки Laurin & Klement и появилась новая компания — Škoda. Результат — конвейерное производство элегантных автомобилей, оснащенных множеством великолепных элементов и выигрывавших международные соревнования. Однако название «Laurin & Klement» сохранилось до наших дней как обозначение внутренней отделки салона автомобилей по классу «люкс».

Великая депрессия замедлила развитие Škoda, но не остановила его. Появилась ещё одна удачная модель — Popular.

Начиная с 1 января 1930 года завод в Млада-Болеславе был преобразован в «Акционерную компанию автомобильной промышленности» (ASAP). В компанию вошёл автозавод в Млада-Болеславе, отделение в Пльзене, ремонтное предприятие «Элка», филиалы, ремонтные предприятия и коммерческие представительства.

В середине 30-х годов на Škoda был разработан проект легкого танка LT vz.35. До 1939 года было выпущено около 300 танков (заводы Шкода выпускали танки по проекту другой чешской компании ЧКД, но оснащали их 37-мм пушками собственной марки), которые после оккупации Чехословакии вошли в состав частей вермахта, участвовали в военных действиях против Франции, Польши, СССР. Последний танк чешского производства был уничтожен в 1941 г. в ходе разгрома немецких войск под Москвой.

В годы Второй мировой войны Škoda стала частью экономической системы Третьего рейха и практически полностью переключилась на выпуск военной продукции. Заводы выпускали газогенераторные грузовики, гусеничные тягачи, а на базе разработанной в 1936 году изысканной модели Škoda Superb выпускались армейские внедорожники. Завод выпускал вооружение для нацистских войск и потому в 1945 году до 70 % заводских мощностей были уничтожены англо-американскими бомбардировками.

С окончанием войны произошли перемены. Работа всей промышленности планировалась централизованно. Обмен информацией с развивающимися западными партнёрами был прекращен. Переименованная в национальное предприятие, AZNP Škoda, фирма стала монополистом на рынке частных автомобилей Чехословакии.

Благодаря прочной базе, традиционным процессам производства и прошлым успехам Чехословакия смогла развиваться и в послевоенный социалистический период, несмотря на информационный вакуум и влияние существующего общественного устройства.

Появились новые модели — Tudor, Spartak, Octavia и Felicia. Однако с появлением на Западе в 1960-е годы новых технологий промышленность страны стала отставать.

Начиная с 1970 года в экономике страны начался застой. Завод смог сохранить лидирующее положение только на рынках восточноевропейских стран. Прорыв наступил лишь в 1987 году с выпуском новой модели — Favorit.

Политические перемены в 1989 году изменили экономическую обстановку. Škoda начала искать сильного зарубежного партнёра для инвестиций и технологической поддержки.

В декабре 1990 года правительство выбрало в качестве партнёра концерн Volkswagen AG. 16 апреля 1991 года был подписан договор о продаже доли в 31 % в собственности Škoda, a.a.s. компании VAG за 620 млн марок. Таким образом Škoda присоединилась в качестве четвёртого бренда к концерну, уже объединявшему марки Volkswagen, Audi и SEAT.

В декабре 1995 года VAG нарастил свою долю до 70 % заплатив 1,4 млрд марок.

В январе 1998 года компания изменила название на Škoda Auto, a.s. Окончательный контроль VAG получил в мае 2000 года, когда за 12,3 млрд крон была выкуплена доля в 30 %. Продолжалось дальнейшее развитие компании. Благодаря взаимодействию с западными партнёрами и новому экономическому мышлению Škoda провела мероприятия по модернизации техническо-производственной базы. В результате этого появилась конкурентоспособная модель — Felicia. За ней последовали и более успешные модели — Octavia, Fabia и гордость компании — Škoda Superb.

В 2011 году концерн представил реинкарнацию модели Rapid, призванную занять нишу бюджетных автомобилей класса В. Весной 2014 года лифтбек Rapid начали продавать на рынках восточной Европы.</a>
